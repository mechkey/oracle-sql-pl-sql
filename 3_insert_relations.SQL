CREATE OR REPLACE PROCEDURE INSERT_TUTORING
( T_STUDENT_IN IN STUDENT.PID%TYPE, T_STAFF_IN IN STAFF.PID%TYPE, T_START_IN TUTORING.TUTOR_START_DATE%TYPE )
AS
	P_ID NUMBER;
    TID_STAFF STAFF.PID%TYPE ;
	TID_STU STUDENT.PID%TYPE ;
	STU_IN_TUT STUDENT.PID%TYPE ; 
	P_STAFF_BRANCH_ID VARCHAR(6) ;
	P_STU_BRANCH_ID VARCHAR(6) ;
	NOT_SAME_BRANCH EXCEPTION ;
	NOT_SAME_TID EXCEPTION;
	INSERTION_ERROR EXCEPTION;
	STUDENT_EXISTS EXCEPTION;
	OTHER_EXCEPTION EXCEPTION;

	PRAGMA EXCEPTION_INIT(NOT_SAME_BRANCH,-20202);
	PRAGMA EXCEPTION_INIT(NOT_SAME_TID,-20203);
	PRAGMA EXCEPTION_INIT(INSERTION_ERROR,-20204);
	PRAGMA EXCEPTION_INIT(STUDENT_EXISTS,-20205);
	
BEGIN
	DBMS_OUTPUT.PUT_LINE ('DISPLAYING POSTGRADUATE STUDENT DETAILS');

	--CHECK STUDENT_ID NOT ALREADY IN TUTORING TABLE
	BEGIN 	
		SELECT S.PID INTO STU_IN_TUT
				FROM STUDENT S, TUTORING T
				WHERE REF (S) = T.STUDENT_ID
				AND S.PID = T_STUDENT_IN;
				DBMS_OUTPUT.PUT_LINE('STUDENT INPUT: '||T_STUDENT_IN);
				DBMS_OUTPUT.PUT_LINE('STUDENT INPUT CHECK: '||STU_IN_TUT) ;
	EXCEPTION
		WHEN NO_DATA_FOUND THEN 
			STU_IN_TUT := NULL;
	END;
	IF STU_IN_TUT IS NULL THEN
			-- TAKE INPUT STUDENT ID AND INSERT STUDENT'S BRANCH ID INTO P_STU_BRANCH_ID (VIA TRACK, ETC)
			SELECT (SELECT DEREF(C.UNDER_BRANCH_ID).BRANCH_ID
					FROM STUDENT S, TRACK_STUDENT CS, TRACK C, BRANCH D
					WHERE S.PID = DEREF(CS.STUDENT_ID).PID
					AND C.TRACK_ID = DEREF(CS.TRACK_ID).TRACK_ID
					AND C.UNDER_BRANCH_ID = REF (D)
					AND S.PID = T_STUDENT_IN
					)
					INTO P_STU_BRANCH_ID FROM DUAL WHERE DUMMY = 'X';
			-- TAKE INPUT STAFF ID AND INSERT STAFF'S BRANCH ID INTO P_STAFF_BRANCH_ID 
			SELECT (SELECT DEREF(F.WORKS_FOR_BRANCH_ID).BRANCH_ID
					FROM  STAFF F
					WHERE F.PID = T_STAFF_IN
					) 
					INTO P_STAFF_BRANCH_ID FROM DUAL WHERE DUMMY = 'X';
	ELSIF STU_IN_TUT IS NOT NULL THEN
		RAISE STUDENT_EXISTS;
	END IF;
	-- CHECK THEY ARE THE SAME
	IF P_STAFF_BRANCH_ID = P_STU_BRANCH_ID
		THEN
			-- INSERT INFORMATION INTO TUTORING, STAFF AND STUDENT
			INSERT INTO TUTORING (STAFF_ID,STUDENT_ID,TUTOR_START_DATE) VALUES ((SELECT      REF(F)  FROM    STAFF   F
				WHERE F.PID = T_STAFF_IN) , (SELECT REF(S) FROM STUDENT S WHERE S.PID = T_STUDENT_IN), T_START_IN) ;
			INSERT INTO TABLE ( SELECT S.TUTEE_REL FROM STUDENT S WHERE S.PID = T_STUDENT_IN) SELECT REF (T) FROM TUTORING T WHERE T.STUDENT_ID.PID IN ( T_STUDENT_IN ) ;
			INSERT INTO TABLE ( SELECT F.TUTOR_REL FROM STAFF F   WHERE F.PID = T_STAFF_IN) SELECT REF (T) FROM TUTORING T WHERE T.STAFF_ID.PID IN ( T_STAFF_IN ) ;
		ELSE	
			RAISE NOT_SAME_BRANCH;
	END IF; 
	-- SELECT THE ID OF THE STUDENT'S TUTORING TABLE RELATIONSHIP INTO TID_STU
	SELECT T.ID INTO TID_STU
			FROM STUDENT S, TRACK_STUDENT CS, TRACK C, BRANCH D, TUTORING T
            WHERE REF (S) = T.STUDENT_ID
            AND S.PID = DEREF(CS.STUDENT_ID).PID
            AND C.TRACK_ID = DEREF(CS.TRACK_ID).TRACK_ID
            AND C.UNDER_BRANCH_ID = REF (D)
            AND S.PID = T_STUDENT_IN
			;
	-- SELECT THE ID OF THE STUDENT'S TUTORING TABLE RELATIONSHIP INTO TID_STAFF
    SELECT (SELECT T.ID
				FROM BRANCH D,  STAFF F, TUTORING T
				WHERE F.WORKS_FOR_BRANCH_ID = REF (D)
				AND DEREF(T.STAFF_ID).PID = F.PID
				AND F.PID = T_STAFF_IN
			MINUS
				(SELECT T.ID
				FROM BRANCH D,  STAFF F, TUTORING T
				WHERE F.WORKS_FOR_BRANCH_ID = REF (D)
				AND DEREF(T.STAFF_ID).PID = F.PID
				AND F.PID = T_STAFF_IN
			MINUS
				SELECT T.ID
				FROM STUDENT S, TRACK_STUDENT CS, TRACK C, BRANCH D, TUTORING T
				WHERE REF (S) = T.STUDENT_ID
				AND S.PID = DEREF(CS.STUDENT_ID).PID
				AND C.TRACK_ID = DEREF(CS.TRACK_ID).TRACK_ID
				AND C.UNDER_BRANCH_ID = REF (D)
				AND S.PID = T_STUDENT_IN)
			)
			INTO TID_STAFF FROM DUAL WHERE DUMMY = 'X';
	-- IF STUDENT AND STAFF HAVE THE SAME TUTORING ID
	IF TID_STU = TID_STAFF
		THEN
		DBMS_OUTPUT.PUT_LINE('----------------------------------------------------');
		DBMS_OUTPUT.PUT_LINE('TUTORING RELATIONSHIP ENTERED SUCCESSFULLY!');
		DBMS_OUTPUT.PUT_LINE('STAFF AND STUDENT HAVE THE SAME BRANCH AND');
		DBMS_OUTPUT.PUT_LINE('TUTORING, STAFF AND STUDENT TABLES HAVE BEEN UPDATED.');
		DBMS_OUTPUT.PUT_LINE('----------------------------------------------------');
		COMMIT;
	ELSE
		RAISE NOT_SAME_TID;
		DBMS_OUTPUT.PUT_LINE(' OPERATION FAILED, ROLLBACK. TID_STU =/= TID_STAFF (P_ID) '||TID_STU||TID_STAFF||P_ID);
		ROLLBACK;
	END IF;
	EXCEPTION 
		WHEN STUDENT_EXISTS THEN
			DBMS_OUTPUT.PUT_LINE('-------------------------------------------');
			DBMS_OUTPUT.PUT_LINE('ERROR! STUDENT ALREADY IN TUTORING DATABASE');
			DBMS_OUTPUT.PUT_LINE('-------------------------------------------');
		WHEN NOT_SAME_BRANCH THEN 
			DBMS_OUTPUT.PUT_LINE('-------------------------------------------');
			DBMS_OUTPUT.PUT_LINE('STUDENT IN BRANCH: '||P_STU_BRANCH_ID);
			DBMS_OUTPUT.PUT_LINE('STAFF IN BRANCH: '||P_STAFF_BRANCH_ID);
			DBMS_OUTPUT.PUT_LINE('STUDENT AND STAFF DO NOT HAVE THE SAME BRANCH ID');
			DBMS_OUTPUT.PUT_LINE('-------------------------------------------');
			ROLLBACK;
		WHEN NOT_SAME_TID THEN
			DBMS_OUTPUT.PUT_LINE ('------------------------------------------------------');
			DBMS_OUTPUT.PUT_LINE ('ERROR! STUDENT AND STAFF DO NOT HAVE THE SAME TUTORING ID');
			DBMS_OUTPUT.PUT_LINE ('------------------------------------------------------');
			ROLLBACK;
		WHEN TOO_MANY_ROWS THEN
			DBMS_OUTPUT.PUT_LINE('-----------------------------------------------------');
			DBMS_OUTPUT.PUT_LINE('ERROR! STUDENT AND/OR STAFF ALREADY IN TUTORING DATABASE');
			DBMS_OUTPUT.PUT_LINE('-----------------------------------------------------');
			ROLLBACK;
		WHEN OTHERS THEN
			DBMS_OUTPUT.PUT_LINE('------------------------------------');
			DBMS_OUTPUT.PUT_LINE ('INPUT ERROR: REASON UNKNOWN '||SQLERRM); 
			DBMS_OUTPUT.PUT_LINE('------------------------------------');
			ROLLBACK;
END;
/

